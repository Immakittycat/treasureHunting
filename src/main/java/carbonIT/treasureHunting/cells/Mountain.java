package carbonIT.treasureHunting.cells;

public class Mountain extends Cell {

	public Mountain() {
		super();
		this.setWalkable(false);
	}

	@Override
	public int hashCode() {
		return super.hashCode();
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		return true;
	}
	
	
}
